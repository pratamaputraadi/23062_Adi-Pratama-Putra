public class HurufZSampaiA {
    public static void main(String[] args) {
       
        // HURUF A-Z UPPERCASE
        System.out.print("MENGURUTKAN HURUF Z HINGGA A UPPERCASE : ");
        System.out.print("\n");
        for (char a = 'Z'; a >= 'A'; a--) {         
            System.out.print ( a + ", ");
        }
        
        // HURUF A-Z LOWERCASE
        System.out.print("\n\nMENGURUTKAN HURUF Z HINGGA A LOWERCASE : ");
        System.out.print("\n");
        for (char a = 'z'; a >= 'a'; a--) {
            System.out.print ( a + ", ");
        }
        
        
        System.out.print("\n\n");
    }
}

